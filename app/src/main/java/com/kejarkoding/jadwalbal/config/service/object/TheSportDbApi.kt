package com.kejarkoding.jadwalbal.config.service.`object`

import android.net.Uri
import com.kejarkoding.jadwalbal.BuildConfig

object TheSportDbApi {
    fun getSchedulePrevMatch(id: String?): String {
        return Uri.parse(BuildConfig.BASE_URL).buildUpon()
                .appendPath("api")
                .appendPath("v1")
                .appendPath("json")
                .appendPath(BuildConfig.TSDB_API_KEY)
                .appendPath("eventspastleague.php")
                .appendQueryParameter("id", id)
                .build()
                .toString()
    }
    fun getScheduleNextMatch(id: String?): String {
